public class PrettyPrinting {
    public static void main(String[] args) {
        float f = 453.2345f;
        // how to print float numbers with what point numbers you need
        System.out.printf("formatted float is %.2f",f);


        // value of PI
        System.out.printf("PI : %.3f",Math.PI);
        System.out.println();

        System.out.printf("%.2f" ,Math.PI);

        //printing strings
        System.out.printf("my name is %s and iam %s","saran","cool");


    }
}
